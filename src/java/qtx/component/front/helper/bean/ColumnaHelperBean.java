package qtx.component.front.helper.bean;

public class ColumnaHelperBean {

	private char visible;
	private String columNom;
	private char control;
	private String descripcion;
	
	public String getColumNom() {
		return columNom;
	}
	public void setColumNom(String columNom) {
		this.columNom = columNom;
	}
	public char getControl() {
		return control;
	}
	public void setControl(char control) {
		this.control = control;
	}
	public String getDescripcion() {
		return descripcion;
	}
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	public char getVisible() {
		return visible;
	}
	public void setVisible(char visible) {
		this.visible = visible;
	}
}
